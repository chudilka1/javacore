package com.chudilka1.app.homework.lesson3;


public class UnitsToBeCheked {
    public int adding (int a, int b, int c) {return a + b + c;}
    public int substraction (int d, int e, int f) {return f - e - d;}
    public int multiplyingByZero (int g) {return g*0;}
    public int complexCalculations (int h, int i, int j, int k, int l) {return (h+i) - j + k*l;}
    public int average (int m, int n, int o, int p, int q) {return (m+n+o+p+q)/5;}
    public double addingDoubles (double r, double s) {return r + s;}
    public double substractingDoubles (double t, double u) {return t - u;}
    public double multiplyingDoubles (double v, double w) {return v*w;}
    public int remaining (int x, int y) {return x % y;}
    public double complexDoubles (double a, double b, double c, double d) {return (a+b)/(c+d);}
    public char equalChars (char a, char b, char c, char d) {return a = b = c = d;}
    public String simpleWord2 (char a, char b, char c) {return new StringBuilder().append(a).append(b).append(c).toString();}
    public String simpleWord3 (char a, char b, char c, char d, char e) {return new StringBuilder().append(a).append(b).append(c).append(d).append(e).toString();}
    public float addingFloats (float a, float b, float c) {return a+b+c;}
    public float substructingFloats (float a, float b) {return a-b;}
    public float multiplyingFloats (float a, float b) {return a*b;}
    public float complexFloats (float a, float b, float c, float d) {return (a+b)-c*d;}
    public boolean trueOrFalse (boolean a) {return a;}
}
